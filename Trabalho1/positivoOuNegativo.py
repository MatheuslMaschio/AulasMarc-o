######## Faça um Programa que peça um valor e mostre na tela se o valor é positivo ou negativo. - Obrigatorio

valor1 = int(input("Insira o número:"));

if (valor1 > 0):
    print("O número é positivo!!");

elif(valor1 == 0):
    print("O valor é neutro!!");

else:
    print("O valor é negativo!!!");
